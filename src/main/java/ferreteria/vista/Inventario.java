private void cargarTablaUsuarios(){
DefaultTableModel modelo = new DefaultTableModel();
IProductosDAO productoDAO = new ProductosDAO();
List<Productos> productos;
modelo.addColumn("ID");
modelo.addColumn("Producto");
modelo.addColumn("Precio");
modelo.addColumn("Cantidad");        
modelo.addColumn("id_Marca");
productos = productoDAO.listarProductos();
for(Productos produc :productos ){
    Object[] llenar = new Object[]{
        
        produc.getIdProductos(),
      produc.getNombreProducto(),
      produc.getPrecio(),
        produc.getCantidad(),
        produc.getId_marca(),
        
    };
    modelo.addRow(llenar);
}
tblInventario.setModel(modelo);

}
private void cargarMarcas(){
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Marca");
    
    IMarcasDAO marcasDAO = new MarcasDAO();
    List<Marcas> marcas = marcasDAO.listarAMrcas();
    for(Marcas marca : marcas){
        Object[] llenar = new Object[]{
            marca.getId_marca(),
        marca.getMarca(),
        };
        modelo.addRow(llenar);
    }
    tblMarcas.setModel(modelo);
    
}
    

