    private void btnAgregarProcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProcActionPerformed
        // TODO add your handling code here
        // nombre, precio , cantidad, marca
        IProductosDAO productosD = new ProductosDAO();
        try {
            var nombre = this.txtNombre.getText();
            var precio = Double.parseDouble(this.txtPrecio.getText());
            var cantidad = Integer.parseInt(this.txtCantidad.getText());
            var marca = Integer.parseInt(this.txtMarca.getText());
            
            if (!(marca >=1 && marca <=3)){
                JOptionPane.showMessageDialog(this, "No existe una marca con el id: "+marca);
            }
            else{
                
            
            Productos productoAdd = new Productos(nombre,precio,cantidad,marca); 
            boolean validar = productosD.agregarProductos(productoAdd);
            cargarTablaUsuarios();
            if(validar){
                JOptionPane.showMessageDialog(this, "producto Agregado correctamente");
                this.txtNombre.setText("");
                this.txtPrecio.setText("");
                this.txtMarca.setText("");
                this.txtCantidad.setText("");
            }else{
                JOptionPane.showMessageDialog(this, "producto no agregado");
            }
        }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Por favor ingrese datos que sean validos");
        }
        
    }//GEN-LAST:event_btnAgregarProcActionPerformed

    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        // TODO add your handling code here:
        IProductosDAO obtenerID = new ProductosDAO();
        
        try{
            
        var eliminarId = Integer.parseInt(this.txtId.getText());
        Productos producto = new Productos (eliminarId);
        boolean existe = obtenerID.eliminarProducto(producto);
        if(existe){
            JOptionPane.showMessageDialog(this,"Producto eliminado");
            cargarTablaUsuarios();
        }else{
            JOptionPane.showMessageDialog(this,"Este ID de producto no existe");
        }
        
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Ingrese un valor valido");
        }
        
        
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

