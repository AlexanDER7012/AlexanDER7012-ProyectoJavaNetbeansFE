public void cargarRestantes(){
    DefaultTableModel modelo = new DefaultTableModel();
    IProductosDAO productoDAO = new ProductosDAO();
List<Productos> productos;
modelo.addColumn("ID");
modelo.addColumn("Producto");
modelo.addColumn("Precio");
modelo.addColumn("Cantidad");        
modelo.addColumn("id_Marca");
productos = productoDAO.listarProductosEscasos();
for(Productos produc :productos ){
    Object[] llenar = new Object[]{
        
        produc.getIdProductos(),
      produc.getNombreProducto(),
      produc.getPrecio(),
        produc.getCantidad(),
        produc.getId_marca(),
        
    };
    modelo.addRow(llenar);
}
tblProductos.setModel(modelo);
    
    
    
}
