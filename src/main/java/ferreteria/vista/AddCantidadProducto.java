    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        IProductosDAO producDAO = new ProductosDAO();
        
         var txtid =this.txtID.getText();
        var txtcantidad = this.txtCantidad.getText();
        if(txtid.isEmpty()||txtcantidad.isEmpty()){
             JOptionPane.showMessageDialog(this, "Por favor llene los espacios");
             return;
        }
 
        try{
       var id =Integer.parseInt(this.txtID.getText());
        var cantidad = Integer.parseInt(this.txtCantidad.getText());     
       
        Productos producto1 = new Productos(cantidad,id);
        var verificar = producDAO.modificarProductos(producto1);
        
        
        if(verificar){
            
            JOptionPane.showMessageDialog(this, "Cantidad Agregada correctamente");
            cargarRestantes();
        }else{
            JOptionPane.showMessageDialog(this, "Error al agregar cantidad al Producto");
        }
         }catch(Exception e){
             System.out.println(e.getMessage());
         }
    }//GEN-LAST:event_btnAddActionPerformed

