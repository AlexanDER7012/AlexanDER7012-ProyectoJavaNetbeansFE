    private void btnClienteExisteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteExisteActionPerformed
        // TODO add your handling code here:
        if (this.rbtnSi.isSelected()) {
            IClientesDAO clientesDAO = new ClientesDAO();
            var clienteExistente = this.txtIdentificacion.getText().trim();
           try{
               
           
            if(!(clienteExistente.equals(""))){
                Integer id =clientesDAO.obtenerIdPorIdentificacion(clienteExistente);
                if(id == null){
                    JOptionPane.showMessageDialog(this, "No se encontro el cliente con esta identificacion");
                }else{
                    
                
               Clientes cliente = clientesDAO.buscarClientes(id);
               LocalDate fechaHoy = LocalDate.now();
             
             this.txtFechaVenta.setText(String.valueOf(fechaHoy));
                this.txtNombre.setText(cliente.getNombre());
                this.txtApellido.setText(cliente.getApellido());
                this.txtTelefono.setText(cliente.getTelefono());
               
                this.txtGmail.setText(cliente.getCorreoElectonico());
               
                JOptionPane.showMessageDialog(this, "Cliente encontrado!");
             }   
            }else {
                 JOptionPane.showMessageDialog(this, "Llenar identificacion");
            }
            }catch (Exception e){
                JOptionPane.showMessageDialog(this, "Este usuario no existe");
            }
           } 
    }//GEN-LAST:event_btnClienteExisteActionPerformed

    private void btnTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerminarActionPerformed
        // TODO add your handling code here:
        IVentasDAO ventasDAO = new VentasDAO();
        var confirmN=this.txtNombre.getText();
        var confirmA=this.txtApellido.getText();
        var confirmD=this.txtFechaVenta.getText();
       String identificacion = this.txtIdentificacion.getText().trim();
       double total = Double.parseDouble(this.txtTotal.getText());
       
        if(confirmN.isEmpty()||confirmA.isEmpty()||confirmD.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe llenar los cammpos: Nombre, Apellido, Fecha");
        }else{
            
        
       if(this.rbtnSi.isSelected()) {
        
        if(!identificacion.equals("")) {
            IClientesDAO clientesDAO = new ClientesDAO();
            Integer id = clientesDAO.obtenerIdPorIdentificacion(identificacion);
            
            
            LocalDateTime fechaLocal = LocalDateTime.now();
            Timestamp fechaSQL = Timestamp.valueOf(fechaLocal);
            Ventas venta = new Ventas(id,fechaSQL,total);
            ventasDAO.agregarVentas(venta);
  
            

            if(id != null){
                clientesDAO.agregarPunto(id); 
                JOptionPane.showMessageDialog(this, "Venta finalizada. Se ha sumado 1 punto al cliente.");
             GeneradorFactura.generarFactura(confirmN, confirmA,identificacion, tblProductos, total);
                MenuEmpleado empleado = new MenuEmpleado();
      empleado.setVisible(true);
      this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "No se encontró cliente con esa identificación.");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debe ingresar una identificación.");
        }
    }else{
        JOptionPane.showMessageDialog(this, "Venta finalizada (sin puntos, cliente no registrado).");
        GeneradorFactura.generarFactura(confirmN, confirmA, identificacion, tblProductos, total);
                Menu menu = new Menu();
            menu.setVisible(true);
            this.dispose();
    }
    }
    }//GEN-LAST:event_btnTerminarActionPerformed

