    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here:
        IClientesDAO clientesDAO = new ClientesDAO();
        var nombre = this.txtNombre.getText();
        var apellido = this.txtApellido.getText();
        var telefono = this.txtTelefono.getText();
        var correoElectronico = this.txtGmail.getText();
        String fechaTexto = this.txtFechaVenta.getText();
        var identificacion = this.txtIdentificacion.getText();
        try {
             
        if (this.rbtnSi.isSelected()) {
            if(nombre.isEmpty()||apellido.isEmpty()||correoElectronico.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe llenar los cammpos: Nombre, Apellido, Gmail, telefono");
        }
            else{
                
            
        
            if (!correoElectronico.matches("^[^@\\s]+@[^@\\s]+\\.com$")) {
            JOptionPane.showMessageDialog(this, "Correo electrónico inválido. Debe contener '@' y terminar en '.com'");
            return;
        }

        if (!telefono.matches("^[0-9]{8}$")) {
            JOptionPane.showMessageDialog(this, "Teléfono inválido. Debe contener exactamente 8 dígitos.");
            return;
        }
        if (nombre.length()<3) {
                JOptionPane.showMessageDialog(this, "El nombre debe tener al menos 3 caracteres.");
                return;
            }
       }
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate fechaLocal = LocalDate.parse(fechaTexto, formato);
        java.sql.Date fechaSQL = java.sql.Date.valueOf(fechaLocal);
        
            Clientes cliente = new Clientes(nombre, apellido, telefono, correoElectronico, fechaSQL, identificacion);
            clientesDAO.agregarClientes(cliente);
            JOptionPane.showMessageDialog(this, "Cliente agregado correctamente.");
            
        }else if(this.rbtnNo.isSelected()){
            if(nombre.isEmpty()||apellido.isEmpty()){
             JOptionPane.showMessageDialog(this,"Debe contener el Nombre y el Apellido");
            }
            else{
                
            
             LocalDate fechaHoy = LocalDate.now();
             
             this.txtFechaVenta.setText(String.valueOf(fechaHoy));
             this.txtIdentificacion.setText("CONSUMIDOR FINAL");
             this.txtGmail.setText("NULL");
             this.txtTelefono.setText("NULL");
          }   
        }
        else {
            JOptionPane.showMessageDialog(this, "Error, seleccione una opcion.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al registrar cliente, todos los datos deben estar llenos ");
    }
      
    
    }//GEN-LAST:event_btnregistrarActionPerformed

