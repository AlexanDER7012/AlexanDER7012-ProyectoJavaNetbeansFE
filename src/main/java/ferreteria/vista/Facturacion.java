    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        IProductosDAO productoDAO = new ProductosDAO();
        Productos producto;
        try {
            
          int idbuscar =Integer.parseInt(this.txtIdProductoB.getText());    
           producto = productoDAO.buscarProducto(idbuscar);
           if(producto!=null){
               
               
               this.txtxNombreP.setText(producto.getNombreProducto());
               this.txtPrecio.setText(String.valueOf(producto.getPrecio()));
               
           }else {
               JOptionPane.showMessageDialog(null, "No existe el producto");
           }
      
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Por favor ingrese datos que sean validos");
        }
        
               
      
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) tblProductos.getModel();

    try {
        int id = Integer.parseInt(txtIdProductoB.getText());
        String nombre = txtxNombreP.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());

        IProductosDAO productosDAO = new ProductosDAO();
        boolean actualizado = productosDAO.restarCantidadProducto(id, cantidad);
        if (actualizado){
        modelo.addRow(new Object[]{id, nombre, precio, cantidad});

        txtIdProductoB.setText("");
        txtxNombreP.setText("");
        txtPrecio.setText("");
        txtCantidad.setText("");
    }else{
            JOptionPane.showMessageDialog(this, "No hay suficiente stock del producto.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Asegúrese de que todos los campos tengan valores válidos.");
    }
        
    }//GEN-LAST:event_jButton2ActionPerformed

